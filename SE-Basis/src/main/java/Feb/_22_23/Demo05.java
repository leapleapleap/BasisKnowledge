package Feb._22_23;

public class Demo05 {
    public static void main(String[] args) {
        //转义字符——>换行 \n
        System.out.println("转义字符：换行");
        System.out.print('\n');
        //回车符   \r
        System.out.println("转义字符：回车");
        System.out.println('\r');
        //双引号   \"
        System.out.println('\"');
        //单引号   \'
        System.out.println('\'');
        //反斜杠   \\
        System.out.println('\\');
        //制表符   \t
        System.out.println("a" + '\t' + "b");
        //退格符   \b
        System.out.print(111);
        System.out.println("\b");
    }
}
