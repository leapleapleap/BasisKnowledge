package part_03.Apr01.demo01;

/**
 * 计算器
 */

public class Calculator {
    /**
     * @param a 参数a
     * @param b 参数b
     * @return 返回两数的和
     */
    public int add(int a, int b) {
        return a + b;
    }

    /**
     * @param a 参数a
     * @param b 参数b
     * @return 返回两数的差
     */
    public int sub(int a, int b) {
        return a - b;
    }
}
